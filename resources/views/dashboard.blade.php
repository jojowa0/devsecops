<p class="text-gray-500 dark:text-gray-400" align="center">
Fruit d’un partenariat entre la Fondation qatarie de Bienfaisance
« Jassim et Hamad Bin Jassim » et le ministère de la santé et de la protection sociale,
en coordination avec le conseil de la région Marrakech-Safi, cet hôpital comporte notamment
un service d’urgences, de réanimation, des maladies internes, un service de radiologie, en plus
d’un laboratoire d’analyses médicales et une pharmacie.
Réalisé également dans le cadre d’une convention de partenariat signée depuis 2017, entre le Conseil de la région Marrakech-Safi et ladite fondation qatarie, cet hôpital prodiguera des prestations dans plusieurs spécialités médicales, dont notamment la gynécologie-obstétrique, la pédiatrie, la médecine générale et la chirurgie générale. S’exprimant à l’occasion de l’inauguration de cet hôpital, le ministre de la santé et de la protection sociale, Khalid Aït Taleb, a souligné que la réalisation de cette structure de santé rentre dans le cadre des initiatives de développement visant à renforcer davantage la coopération maroco-qatarie dans les domaines sociaux vitaux.
Il a relevé que cette structure de santé est de nature à prodiguer des prestations de qualité aux populations, dont le nombre est estimé à 160.000 usagers annuellement.
Dans ce contexte, Goudar a relevé que la Fondation qatarie de Bienfaisance Jassim et Hamad Bin Jassim a financé la construction et l’équipement d’un autre hôpital à Safi pour une enveloppe budgétaire estimée à 250 millions de dirhams, indiquant que la dite fondation a aussi procédé, en coordination avec le conseil de la région, à la construction de quatre centres d’hémodialyse dans les différentes provinces de la région, pour un coût global de 48 millions de dirhams, ce qui est de nature à contribuer à réduire le nombre de patients inscrits dans la liste des attentes.
Le directeur exécutif de la Fondation qatarie Jassim et Hamad Bin Jassim de Bienfaisance, Said Moudakir Al Hajiri, a pour sa part, souligné que la réalisation de cet hôpital traduit les liens de fraternité unissant les deux pays frères, indiquant que cette structure de santé représente un des projets de santé modèle réalisée en collaboration avec le Conseil de la région Marrakech-Safi.
Et d’ajouter que le projet vient traduire la profonde conviction de la Fondation de l’importance de la promotion de la qualité des systèmes de santé en vue de réaliser un essor global et durable, relevant que cette structure de santé traduit la vision et la volonté commune de promouvoir le secteur de la santé.
Cet hôpital, situé dans une zone géographique vitale, contribuera à renforcer l’offre des soins dans la région, et faciliter l’accès des populations aux prestations sanitaires et promouvoir la qualité des soins, a-t-il noté, estimant que la fondation a œuvré afin que cette structure de santé comporte plusieurs spécialités vitales.
La cérémonie d’inauguration de cet hôpital s’est déroulée en présence du Wali de la région Marrakech-Safi, gouverneur de la préfecture de Marrakech, M. Karim Kassi-Lahlou, le président du Conseil d’administration de la Fondation qatarie de Bienfaisance Jassim et Hamad Bin Jassim, Cheikh Jabor Bin Youssef Al-Thani et le directeur général de l’Autorité de régulation des activités caritatives (RACA), Ibrahim Abdullah Al-Dehaimi.
